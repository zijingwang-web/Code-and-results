#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
cooccurrence_network_build.py
Builds color co-occurrence networks from the grouped color tables produced by
color_extraction.py.

Core idea:
- Reads image_csccolors_group.csv generated by color_extraction.py.
- Uses colors that already passed the per-image coverage filter.
- Creates one row per image with a semicolon-separated color list.
- Generates a weighted edge list where edge weights represent the frequency
  of two colors appearing together on the same cover.

Outputs:
- image_csccolors_group2.csv: One row per image with semicolon-separated colors.
- image_csccolors_threelist.csv: The weighted edge list for network construction.

Dependencies: pandas, tqdm
"""

import os
from itertools import combinations
import pandas as pd
from tqdm import tqdm


def _clean_color(value):
    """Normalize a color value for stable grouping."""
    if pd.isna(value):
        return ""
    return str(value).strip()


def build_group2_from_group(group_df):
    """
    Converts image_csccolors_group.csv into one row per image.
    Returns columns: filename, css_color_en, color_count
    """
    required = {'filename', 'css_color_en'}
    missing = required - set(group_df.columns)
    if missing:
        raise ValueError(f"image_csccolors_group.csv missing required columns: {sorted(missing)}")

    df = group_df.copy()
    df['css_color_en'] = df['css_color_en'].apply(_clean_color)
    df = df[df['css_color_en'] != '']

    group2 = (
        df.groupby('filename')['css_color_en']
        .agg(lambda values: '; '.join(sorted(set(values))))
        .reset_index()
    )
    group2['color_count'] = group2['css_color_en'].apply(
        lambda x: len([c for c in str(x).split(';') if c.strip()])
    )
    return group2


def generate_color_pairs_for_image(color_string):
    """Returns unique unordered color pairs for one image."""
    colors = sorted({c.strip() for c in str(color_string).split(';') if c.strip()})
    return [(a, b) for a, b in combinations(colors, 2)]


def build_weighted_edges(group2_df):
    """Builds the weighted edge list from per-image color lists."""
    rows = []
    for _, row in tqdm(group2_df.iterrows(), total=len(group2_df), desc="Generating color pairs"):
        for a, b in generate_color_pairs_for_image(row['css_color_en']):
            rows.append({'from': a, 'to': b, 'weight': 1})

    if not rows:
        return pd.DataFrame(columns=['from', 'to', 'weight'])

    edges = pd.DataFrame(rows)
    edges = edges.groupby(['from', 'to'], as_index=False)['weight'].sum()
    edges = edges.sort_values(['weight', 'from', 'to'], ascending=[False, True, True]).reset_index(drop=True)
    return edges


def process_category(category_path, output_root=None):
    """
    Builds co-occurrence files for one category directory.

    category_path can be either:
    - ./output/data/<category>, when output_root is None
    - ./data/images/<category>, when output_root is provided; in this case the
      grouped file is read from ./output/data/<category>.
    """
    category_name = os.path.basename(category_path.rstrip(os.sep))
    output_category = category_path if output_root is None else os.path.join(output_root, category_name)
    os.makedirs(output_category, exist_ok=True)

    group_path = os.path.join(output_category, 'image_csccolors_group.csv')
    if not os.path.exists(group_path):
        print(f"image_csccolors_group.csv not found in {output_category}. Run color_extraction.py first. Skipping.")
        return False

    print(f"\n{'=' * 50}")
    print(f"Building co-occurrence network for category: {category_name}")
    print(f"Reading grouped colors from: {group_path}")
    print(f"{'=' * 50}")

    group_df = pd.read_csv(group_path)
    group2_df = build_group2_from_group(group_df)

    group2_path = os.path.join(output_category, 'image_csccolors_group2.csv')
    group2_df.to_csv(group2_path, index=False)
    print(f"Saved per-image grouped color list to: {group2_path}")

    edges_df = build_weighted_edges(group2_df)
    threelist_path = os.path.join(output_category, 'image_csccolors_threelist.csv')
    edges_df.to_csv(threelist_path, index=False)
    print(f"Network build complete. Saved to {threelist_path}")
    return True


def main():
    OUTPUT_ROOT = './output/data'

    if not os.path.exists(OUTPUT_ROOT):
        print(f"Output directory {OUTPUT_ROOT} not found. Run color_extraction.py first.")
        return

    categories = [os.path.join(OUTPUT_ROOT, d) for d in os.listdir(OUTPUT_ROOT)
                  if os.path.isdir(os.path.join(OUTPUT_ROOT, d))]

    if not categories:
        print(f"No category folders found in {OUTPUT_ROOT}.")
        return

    success_count = 0
    for cat_dir in categories:
        if process_category(cat_dir, output_root=None):
            success_count += 1

    print(f"\nProcessed {success_count}/{len(categories)} categories.")


if __name__ == '__main__':
    main()
